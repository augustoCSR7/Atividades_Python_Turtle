
import turtle

turtle.bgcolor('white')
turtle.pencolor('blue')
turtle.shape('arrow')
turtle.speed(200)
turtle.right(45)

for i in range(200):
    turtle.forward(50)
    turtle.right(90)
    turtle.forward(50)
    turtle.right(90)
    turtle.forward(50)
    turtle.right(90)
    turtle.forward(50)
    turtle.right(88)

turtle.done()